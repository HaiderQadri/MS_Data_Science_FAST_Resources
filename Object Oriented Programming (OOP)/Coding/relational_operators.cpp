#include <iostream>
using namespace std;

int main() //* > greater than, >= greater than or equal to, < less than, <= less than or equal to, == equal to, != not equal to
{
    int a = 23, b = 5, c = 8;
    cout << (a > b) << endl;
    cout << (a < b) << endl;
    cout << (a > c) << endl;
    cout << (a == b) << endl;
    cout << (a != c) << endl;
}